// Helper function to get all resources for a specific category
export const getResourcesByCategory = (category) => {
  const allResources = [];
  
  if (category === 'all') {
    // Return all resources
    Object.values(courseResources).forEach(course => {
      allResources.push(...course.resources);
    });
    allResources.push(...mentorshipResources.resources);
    allResources.push(...coachingResources.resources);
    allResources.push(...counselingResources.resources);
  } else if (category === 'courses') {
    Object.values(courseResources).forEach(course => {
      allResources.push(...course.resources);
    });
  } else if (category === 'mentorship') {
    allResources.push(...mentorshipResources.resources);
  } else if (category === 'coaching') {
    allResources.push(...coachingResources.resources);
  } else if (category === 'counseling') {
    allResources.push(...counselingResources.resources);
  }
  
  return allResources;
};

// Helper function to get resources for enrolled courses only
export const getResourcesForEnrolledCourses = (enrolledCourses, category) => {
  const enrolledCourseIds = enrolledCourses.map(course => course.id);
  const allResources = getResourcesByCategory(category);
  
  if (category === 'courses') {
    return allResources.filter(resource => {
      // Check if this resource belongs to an enrolled course
      return Object.values(courseResources).some(course => 
        course.courseId === parseInt(resource.id.split('_')[1]) && 
        enrolledCourseIds.includes(parseInt(resource.id.split('_')[1]))
      );
    });
  }
  
  return allResources;
};
